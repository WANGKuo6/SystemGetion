package mypackage.model;

public class Equipe {
	private int idEquipe;
	private String nomEquipe;
	private int nbParticipation;
	private int nbVictore;
	
	public int getIdEquipe() {
		return idEquipe;
	}
	public void setIdEquipe(int idEquipe) {
		this.idEquipe = idEquipe;
	}
	public String getNomEquipe() {
		return nomEquipe;
	}
	public void setNomEquipe(String nomEquipe) {
		this.nomEquipe = nomEquipe;
	}
	public int getNbParticipation() {
		return nbParticipation;
	}
	public void setNbParticipation(int nbParticipation) {
		this.nbParticipation = nbParticipation;
	}
	public int getNbVictore() {
		return nbVictore;
	}
	public void setNbVictore(int nbVictore) {
		this.nbVictore = nbVictore;
	}
	
	
}
